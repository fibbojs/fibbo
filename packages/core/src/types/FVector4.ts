/**
 * A 4d vector with x, y, z and w coordinates.
 */
export interface FVector4 {
  /**
   * The x coordinate of the vector.
   */
  x: number
  /**
   * The y coordinate of the vector.
   */
  y: number
  /**
   * The z coordinate of the vector.
   */
  z: number
  /**
   * The w coordinate of the vector.
   */
  w: number
}
